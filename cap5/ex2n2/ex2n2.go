package main

import "fmt"

func main() {
	a := 10 == 0
	b := 10 != 0
	c := 10 <= 0
	d := 10 < 0
	e := 10 >= 0
	f := 10 > 0

	fmt.Println(a)
	fmt.Println(b)
	fmt.Println(c)
	fmt.Println(d)
	fmt.Println(e)
	fmt.Println(f)
}
